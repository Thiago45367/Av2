    private void btnconfimarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconfimarActionPerformed
        // TODO add your handling code here:
        String nomepaciente = txtnome.getText().trim();
        String cpf = txtcpf.getText().trim();
        String telefone = txttelefone.getText().trim();
        
        PacienteController pacientecontroller = new PacienteController();
        
        if (pacientecontroller.cadastrar(nomepaciente, cpf, telefone)) {
            JOptionPane.showMessageDialog(this, "Cadastro Concluido!");
        }
        
        String nomenomemedico = txtnome2.getText().trim();
        String especialidade = txtespecialidade.getText().trim();
        String crm = txtcrm.getText().trim();
        
        MedicoController medicocontroller = new MedicoController();
        
        if (medicocontroller.cadastrar(nomenomemedico, especialidade, crm)) {
            JOptionPane.showMessageDialog(this, "Cadastro Concluido!");
        }
    }//GEN-LAST:event_btnconfimarActionPerformed

